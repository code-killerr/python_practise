import turtle as t
t.speed(10)
t.hideturtle()
t.bgcolor('black')
while 1:
    t.pencolor('red')
    t.penup()
    t.goto(0,0)
    t.forward(200)
    t.pendown()
    t.circle(100)
    t.left(2)
    